CREATE TABLE ORACLEGO.STUFFS (
    ID NUMBER,
    NAME VARCHAR2(256),
    CATEGORY VARCHAR2(64),
    DESCRIPTION VARCHAR2(512),
    IMAGE VARCHAR2(512),
    GEO_LOCATION SDO_GEOMETRY,
    LATITUDE NUMBER(*, 6),
    LONGITUDE NUMBER(*, 6)
);

INSERT INTO USER_SDO_GEOM_METADATA
    VALUES (
        'STUFFS',
        'GEO_LOCATION',
        MDSYS.SDO_DIM_ARRAY(
            MDSYS.SDO_DIM_ELEMENT('Longitude', -180.0, 180.0, 0.05),
            MDSYS.SDO_DIM_ELEMENT('Latitude', -90.0, 90.0, 0.05)
        ),
        4326
    );

CREATE INDEX STAFFS_GEO_LOCATION
    ON ORACLEGO.STUFFS(GEO_LOCATION) INDEXTYPE IS MDSYS.SPATIAL_INDEX;
